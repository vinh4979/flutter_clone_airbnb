class UserProfile {
  final int id;
  final String name;
  final String email;
  final String phone;
  final String birthday;
  final String avatar;
  final bool gender;
  final String role;

  UserProfile({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.birthday,
    required this.avatar,
    required this.gender,
    required this.role,
  });
}
